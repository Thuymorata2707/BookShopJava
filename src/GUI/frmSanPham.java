    private void showDataTable() {
            List<SanPham> listSP = ctl.getSanPham();
            for(int i = 0; i < listSP.size(); i++) {
                Object[] rows = new Object[6];
                rows[0] = listSP.get(i).getMaSP();
                rows[1] = listSP.get(i).getTenSP();
                rows[2] = listSP.get(i).getSoLuong();
                rows[3] = listSP.get(i).getMoTa();
                rows[4] = listSP.get(i).getDonGia();
                rows[5] = listSP.get(i).getMaLoaiSP();
                dt.addRow(rows);
            }

   
    }
    
    private void getDataCombo(){
        Controller.LoaiSanPhamController ctlLSP = new Controller.LoaiSanPhamController();
        List<LoaiSanPham> listLSP;
        try {
            listLSP = ctlLSP.getLoaiSanPham();
            for(int i = 0; i < listLSP.size(); i++){
                cmbMaLoaiSP.addItem(listLSP.get(i).getTenLoaiSP());
            }
                
        } catch (Exception e) {
            System.out.println("Loi" + e.getMessage());
        }
    }

