    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        SanPham sp = new SanPham();
        sp.setMaSP(txtMaSP.getText());
        sp.setTenSP(txtTenSP.getText());
        sp.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
        sp.setMoTa(txtMoTa.getText());
        sp.setDonGia(Float.parseFloat(txtDonGia.getText()));
        
        Controller.LoaiSanPhamController lstLsp = new Controller.LoaiSanPhamController();
        List<LoaiSanPham> lstSP = lstLsp.getLoaiSanPham();
        sp.setMaLoaiSP(lstSP.get(cmbMaLoaiSP.getSelectedIndex()).getMaLoaiSP());
        
        boolean check = ctl.ThemSanPham(sp);
        if(check){
            JOptionPane.showMessageDialog(null, "Them thanh cong. ", "Thong bao", 1);
            btnLamMoi.doClick();
        }
        else{
            JOptionPane.showMessageDialog(null, "Them that bai. ", "Thong bao", 1);

        }
        
    }//GEN-LAST:event_btnThemActionPerformed

    private void tbQLSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbQLSPMouseClicked
        // TODO add your handling code here:
        int r = tbQLSP.getSelectedRow();
        if(r>= 0){
            txtMaSP.setText(tbQLSP.getValueAt(r, 0).toString());
            txtTenSP.setText(tbQLSP.getValueAt(r, 1).toString());
            txtSoLuong.setText(tbQLSP.getValueAt(r, 2).toString());
            txtMoTa.setText(tbQLSP.getValueAt(r, 3).toString());
            txtDonGia.setText(tbQLSP.getValueAt(r, 4).toString());
            cmbMaLoaiSP.setSelectedItem(tbQLSP.getValueAt(r, 5).toString());
            
            txtMaSP.setEnabled(false);
            btnThem.setEnabled(false);
            btnSua.setEnabled(true);
            btnXoa.setEnabled(true);
        }
    }//GEN-LAST:event_tbQLSPMouseClicked

