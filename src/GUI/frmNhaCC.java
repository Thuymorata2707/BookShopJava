    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        NhaCungCap sp = new NhaCungCap();
        sp.setMaNCC(txtMaNCC.getText());
        sp.setTenNCC(txtTenNCC.getText());
        sp.setSDT(txtSDT.getText());
        sp.setDiaChi(txtDiaChi.getText());
        
        boolean check = ctl.ThemNhaCungCap(sp);
        if(check){
            JOptionPane.showMessageDialog(null, "Them thanh cong. ", "Thong bao", 1);
            btnLamMoi.doClick();
        }
        else{
            JOptionPane.showMessageDialog(null, "Them that bai. ", "Thong bao", 1);
        }

    }//GEN-LAST:event_btnThemActionPerformed

