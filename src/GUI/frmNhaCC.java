    private void showDataTable() {
            List<NhaCungCap> LNCC = ctl.getNhaCungCap();
            for(int i = 0; i < LNCC.size(); i++) {
                Object[] rows = new Object[4];
                rows[0] = LNCC.get(i).getMaNCC();
                rows[1] = LNCC.get(i).getTenNCC();
                rows[2] = LNCC.get(i).getSDT();
                rows[3] = LNCC.get(i).getDiaChi();
                dt.addRow(rows);
            }

   
    }

