    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        ClearDataTable();
        List<NhaCungCap> LNCC = ctl.TimKiemNhaCungCap(txtTimKiem.getText());
        if(LNCC != null){
            for (int i = 0; i < LNCC.size(); i++) {
                Object[] rows = new Object[4];
                rows[0] = LNCC.get(i).getMaNCC();
                rows[1] = LNCC.get(i).getTenNCC();
                rows[2] = LNCC.get(i).getSDT();
                rows[3] = LNCC.get(i).getDiaChi();
                dt.addRow(rows);
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void jtbNCCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbNCCMouseClicked
        // TODO add your handling code here:
        int r = jtbNCC.getSelectedRow();
        if(r>= 0){
            txtMaNCC.setText(jtbNCC.getValueAt(r, 0).toString());
            txtTenNCC.setText(jtbNCC.getValueAt(r, 1).toString());
            txtSDT.setText(jtbNCC.getValueAt(r, 2).toString());
            txtDiaChi.setText(jtbNCC.getValueAt(r, 3).toString());
             
            txtMaNCC.setEnabled(false);
            btnThem.setEnabled(false);
            btnSua.setEnabled(true);
            btnXoa.setEnabled(true);
        }
    }//GEN-LAST:event_jtbNCCMouseClicked

